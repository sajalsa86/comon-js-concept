//JavaScript type coercion explained
var num = 5;
var str = "5";

if (num == str) {
    console.log("Equal");
} else {
    console.log("Not Equal");
}

// Output: "Equal"

var num = 5;
var str = "5";

if (num === str) {
    console.log("Equal");
} else {
    console.log("Not Equal");
}

// Output: "Equal"